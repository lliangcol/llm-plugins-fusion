#!/usr/bin/env node
/** Generate cross-assistant adapter inventories from the canonical workflow spec. */

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const sourcePath = 'workflow-specs/workflows.json';

function loadSpec() {
  return JSON.parse(readFileSync(resolve(root, sourcePath), 'utf8'));
}

function genericManifest(spec) {
  return {
    schemaVersion: 1,
    source: sourcePath,
    compatibilityLevel: 'L1-parseable',
    claimBoundary: 'Invocation and enforcement require an assistant-specific adapter and conformance evidence.',
    workflows: spec.workflows.map((workflow) => ({
      id: workflow.id,
      contract: `../../nova-plugin/${workflow.contractPath}`,
      stage: workflow.stage,
      risk: workflow.risk,
      requiredInputs: workflow.requiredInputs,
      outputContract: workflow.outputContract,
      capabilities: spec.permissionProfiles[workflow.permissionProfile].capabilities,
    })),
  };
}

function codexAgents(spec) {
  const rows = spec.workflows.map((workflow) => `| ${workflow.id} | nova-${workflow.id} | ${workflow.stage} | ${workflow.ownerAgents.join(', ')} | ${workflow.recommendedPacks.join(', ') || 'None'} | ${workflow.risk} | ${workflow.requiredInputs.join(', ') || 'None'} | ${workflow.outputContract} |`).join('\n');
  return `# Generated Codex Workflow Adapter

Status: generated from \`${sourcePath}\`. Do not edit this file by hand.

Codex should treat the referenced \`nova-plugin/skills/nova-*/SKILL.md\` files as behavioral contracts, not as Claude slash-command runtime instructions. Parse the request, select one workflow, enforce the capability boundary in \`adapters/generic-agent-skills/manifest.json\`, then load only that workflow contract. Never claim Claude hooks or permissions are active in Codex.

For write-capable workflows, require explicit approval and remain inside the user-provided workspace. Network, credentials, user-scope mutation, external publish, and Git history mutation are denied by the canonical capability profiles unless a consumer repository separately authorizes them.

| Workflow | Compatibility skill | Stage | Owner agents | Recommended packs | Risk | Required inputs | Output contract |
| --- | --- | --- | --- | --- | --- | --- | --- |
${rows}
`;
}

function claudeManifest(spec) {
  return {
    schemaVersion: 1,
    source: sourcePath,
    compatibilityLevel: 'L4-verified',
    evidence: [
      '../../scripts/validate-plugin-install.mjs',
      '../../scripts/validate-plugin-route-live.mjs',
      '../../.github/workflows/release.yml',
    ],
    commands: spec.workflows.map((workflow) => `/nova-plugin:${workflow.id}`),
    legacyAliases: spec.workflows.map((workflow) => `/nova-plugin:${workflow.legacyAlias}`),
  };
}

export function generatedFiles() {
  const spec = loadSpec();
  return [
    { path: 'adapters/generic-agent-skills/manifest.json', content: `${JSON.stringify(genericManifest(spec), null, 2)}\n` },
    { path: 'adapters/codex/AGENTS.md', content: codexAgents(spec) },
    { path: 'adapters/claude/manifest.json', content: `${JSON.stringify(claudeManifest(spec), null, 2)}\n` },
  ];
}

export function checkOrWrite({ write = false } = {}) {
  const stale = [];
  for (const file of generatedFiles()) {
    const path = resolve(root, file.path);
    if (write) {
      mkdirSync(dirname(path), { recursive: true });
      writeFileSync(path, file.content, 'utf8');
    } else if (!existsSync(path) || readFileSync(path, 'utf8') !== file.content) stale.push(file.path);
  }
  if (stale.length) throw new Error(`${stale.join(', ')} stale; run node scripts/generate-adapters.mjs --write`);
  return generatedFiles().length;
}

export function main(args = process.argv.slice(2)) {
  if (args.some((arg) => arg !== '--write')) return 1;
  try {
    const count = checkOrWrite({ write: args.includes('--write') });
    console.log(`OK assistant adapters (${count} generated files)`);
    return 0;
  } catch (error) {
    console.error(`ERROR ${error.message}`);
    return 1;
  }
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) process.exitCode = main();
