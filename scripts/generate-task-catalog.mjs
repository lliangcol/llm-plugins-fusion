#!/usr/bin/env node

import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { extname, resolve } from 'node:path';

const root = resolve(import.meta.dirname, '..');
const write = process.argv.includes('--write');
const registry = JSON.parse(readFileSync(resolve(root, 'governance/task-registry.json'), 'utf8'));
const packageJson = JSON.parse(readFileSync(resolve(root, 'package.json'), 'utf8'));
const errors = [];
const categoryIds = new Set(Object.keys(registry.categories ?? {}));

function compileRules(rules, label) {
  return (rules ?? []).map((rule, index) => {
    if (!categoryIds.has(rule.category)) errors.push(`${label}[${index}] references unknown category ${rule.category}`);
    try {
      return { ...rule, matcher: new RegExp(rule.pattern, 'u') };
    } catch (error) {
      const failure = error instanceof Error ? error : new Error(String(error));
      errors.push(`${label}[${index}] has invalid pattern: ${failure.message}`);
      return { ...rule, matcher: /$a/u };
    }
  });
}

const scriptRules = compileRules(registry.scriptRules, 'scriptRules');
const npmRules = compileRules(registry.npmScriptRules, 'npmScriptRules');

function walk(directory) {
  const output = [];
  for (const entry of readdirSync(resolve(root, directory), { withFileTypes: true })) {
    const path = `${directory}/${entry.name}`;
    if (entry.isDirectory()) output.push(...walk(path));
    else if (['.mjs', '.sh', '.ps1'].includes(extname(entry.name))) output.push(path);
  }
  return output.sort();
}

function classify(value, rules, label) {
  const match = rules.find((rule) => rule.matcher.test(value));
  if (!match) {
    errors.push(`${label} is not classified: ${value}`);
    return 'unclassified';
  }
  return match.category;
}

const scripts = walk('scripts').map((path) => ({ path, category: classify(path, scriptRules, 'script') }));
const npmScripts = Object.keys(packageJson.scripts ?? {}).sort().map((name) => ({
  name,
  command: packageJson.scripts[name],
  category: classify(name, npmRules, 'npm script'),
}));

function workflowJobs() {
  const output = [];
  const directory = '.github/workflows';
  for (const entry of readdirSync(resolve(root, directory), { withFileTypes: true })) {
    if (!entry.isFile() || !/\.ya?ml$/u.test(entry.name)) continue;
    const path = `${directory}/${entry.name}`;
    const lines = readFileSync(resolve(root, path), 'utf8').split(/\r?\n/u);
    let inJobs = false;
    for (const line of lines) {
      if (line === 'jobs:') {
        inJobs = true;
        continue;
      }
      if (inJobs && /^\S/u.test(line)) inJobs = false;
      const match = inJobs ? line.match(/^  ([A-Za-z0-9_-]+):\s*$/u) : null;
      if (match) output.push({ workflow: path, job: match[1] });
    }
  }
  return output.sort((a, b) => `${a.workflow}:${a.job}`.localeCompare(`${b.workflow}:${b.job}`));
}

const jobs = workflowJobs();

if (errors.length > 0) {
  console.error(`Task registry validation failed (${errors.length} error${errors.length === 1 ? '' : 's'}):`);
  for (const error of errors) console.error(`- ${error}`);
  process.exit(1);
}

const lines = [
  '# Maintainer Task Catalog',
  '',
  '<!-- Generated by node scripts/generate-task-catalog.mjs --write. Do not edit by hand. -->',
  '',
  'This catalog classifies every repository maintenance script and npm shortcut, then maps the current GitHub Actions jobs. The source is `governance/task-registry.json`.',
  '',
  '## Categories',
  '',
  '| Category | Contract |',
  '| --- | --- |',
  ...Object.entries(registry.categories).map(([id, description]) => `| \`${id}\` | ${description} |`),
  '',
  `## Script Inventory (${scripts.length})`,
  '',
  '| Category | Script |',
  '| --- | --- |',
  ...scripts.map((task) => `| \`${task.category}\` | \`${task.path}\` |`),
  '',
  `## npm Shortcuts (${npmScripts.length})`,
  '',
  '| Category | Shortcut | Command |',
  '| --- | --- | --- |',
  ...npmScripts.map((task) => `| \`${task.category}\` | \`npm run ${task.name}\` | \`${task.command.replaceAll('|', '\\|')}\` |`),
  '',
  `## CI Job Map (${jobs.length})`,
  '',
  '| Workflow | Job |',
  '| --- | --- |',
  ...jobs.map((entry) => `| \`${entry.workflow}\` | \`${entry.job}\` |`),
  '',
  '## Change Routing',
  '',
  '- Add or rename a maintenance script: update `governance/task-registry.json` if no existing rule classifies it, then regenerate this catalog.',
  '- Add or rename an npm shortcut: keep `package.json` and the npm classification rules aligned.',
  '- Add or rename a CI job: regenerate this catalog and run `node scripts/validate-github-workflows.mjs`.',
  '- Validate without writing: run `node scripts/generate-task-catalog.mjs`.',
  '',
].join('\n');

const outputPath = resolve(root, registry.generatedOutputs[0]);
if (write) {
  writeFileSync(outputPath, lines, 'utf8');
  console.log(`Wrote ${registry.generatedOutputs[0]}`);
} else if (readFileSync(outputPath, 'utf8') !== lines) {
  console.error(`${registry.generatedOutputs[0]} is stale; run node scripts/generate-task-catalog.mjs --write`);
  process.exit(1);
} else {
  console.log(`OK task registry covers ${scripts.length} scripts, ${npmScripts.length} npm shortcuts, and ${jobs.length} CI jobs`);
}
